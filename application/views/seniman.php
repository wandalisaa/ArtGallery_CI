				<p class="text-center">Frida Kahlo de Rivera adalah seorang pelukis Meksiko yang lahir di Coyoacán, dan paling dikenal karena potret dirinya.
                    Kehidupan Kahlo mulai dan berakhir di Kota Meksiko, di rumahnya yang dikenal sebagai Blue House. Dia menulis tanggal lahirnya sebagai 7 Juli 1910, tetapi akta kelahirannya menunjukkan tanggal 6 Juli 1907. Kahlo diduga menginginkan tahun kelahirannya bertepatan dengan tahun awal revolusi Meksiko sehingga hidupnya akan dimulai dengan hadirnya Meksiko modern. Pada usia enam tahun, Frida mengidap polio yang menyebabkan kaki kanannya tampak lebih kecil dari yang lain, dan ini tetap berlanjut hingga permanen. Karyanya telah dikenal sebagai simbol dari tradisi nasional dan masyarakat adat di Meksiko, dan oleh kaum feminis dianggap sebagai penggambaran yang tak kenal kompromi dari wujud dan pengalaman perempuan.
                    Budaya Meksiko dan tradisi budaya suku Indian penting dalam karyanya yang kadang ditandai baik sebagai seni naivisme maupun seni rakyat. Karyanya juga telah digambarkan sebagai "surealis" dan pada tahun 1938, André Breton, inisiator utama dari gerakan surealis, menggambarkan seni Kahlo sebagai "pita di sekitar bom".</p>
